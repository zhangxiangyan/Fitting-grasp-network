def color_imshow(label):
    # 创建纯黑色的图
    h, w = label.shape
    label_rgb = np.zeros((h, w, 3))

    # 根据分类编号，转换为对应颜色
    for i, rgb in zip(range(len(colors)), colors):
        label_rgb[label == i] = rgb

    return label_rgb.astype(np.uint8)


